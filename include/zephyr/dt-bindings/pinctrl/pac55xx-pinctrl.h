/**
 * @name Qorvo PAC55XX pin configuration bit field positions/masks.
 * @{
 */

/** Port position */
#define PAC55XX_PORT_POS 0U
/** Port mask */
#define PAC55XX_PORT_MSK 0x7U
/** Pin position */
#define PAC55XX_PIN_POS  3U
/** Pin mask */
#define PAC55XX_PIN_MSK  0x7U
/** Multiplexer position */
#define PAC55XX_MUX_POS  6U
/** Multiplexer mask */
#define PAC55XX_MUX_MSK  0x7U

/** @} */

/**
 * @brief Utility macro to build Qorvo PAC55XX pinmux property entry.
 *
 * @param port Port
 * @param pin Pin
 * @param mux Multiplexer
 */
#define PAC55XX_PINMUX(port, pin, mux)                                                             \
	(((((port) - 'A') & PAC55XX_PORT_MSK) << PAC55XX_PORT_POS) |                               \
	 (((pin)&PAC55XX_PIN_MSK) << PAC55XX_PIN_POS) |                                            \
	 (((mux)&PAC55XX_MUX_MSK) << PAC55XX_MUX_POS))

